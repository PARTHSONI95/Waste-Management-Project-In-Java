    public void populateTable() {
        ArrayList<AdminUserDTO> orgUsers = ManageAdminDAO.getOrgUsersDetails(loggedUser);
        DefaultTableModel dtm = (DefaultTableModel) tblOrgUsers.getModel();
        dtm.setRowCount(0);
        int i=0;
        for(AdminUserDTO s: orgUsers){
            
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = s.getUserName();
            row[1] = s.getOrganizationName();
            dtm.addRow(row); 
        }
   
    }
