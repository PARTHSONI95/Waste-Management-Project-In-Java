    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed
     
    String userName = txtUserName.getText();
    ArrayList<String> allUsers = ManageAdminDAO.selectAllAdminUsers();
        if(allUsers.contains(userName)){
        JOptionPane.showMessageDialog(null, "Please select a new Name", "Name Already Present",JOptionPane.WARNING_MESSAGE);
        }else if(!Validation.checkMultiAlphaNumeric(userName) || userName==null || !Validation.checkAlphaNumericSpecial(txtPassword.getText()) || !Validation.checkAlpha(txtCity.getText())){
        JOptionPane.showMessageDialog(null, "Invalid Details", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
        
    AdminUserDTO organizationUser = new AdminUserDTO(txtUserName.getText(),txtPassword.getText(),cmbOrgList.getSelectedItem().toString(),txtCity.getText());
    
    ManageAdminDAO.addOrganizationUserInDB(organizationUser, loggedUser);
        System.out.println("Organization user inserted in DB");
        populateTable();
        }
    }//GEN-LAST:event_createUserJButtonActionPerformed


