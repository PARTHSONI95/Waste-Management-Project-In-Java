    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
    
    String enterpriseName = txtEnterpriseName.getText();
    String netType =  cmbNetType.getSelectedItem().toString();
    boolean checkFlag = false;
    ArrayList<EnterpriseDTO> allEnterprisesList = ManageAdminDAO.selectEnterprise();
    for(EnterpriseDTO enterprise : allEnterprisesList){
        if(enterprise.getEnterpriseName().equals(enterpriseName) && enterprise.getNetworkName().equals(netType)){
        checkFlag = true;
        break;
        }
    }
        
    if(checkFlag){
    JOptionPane.showMessageDialog(null, "Please select a new Name", "Name Already Present",JOptionPane.WARNING_MESSAGE);
    }else if(!Validation.checkMultiAlphaNumeric(enterpriseName) || enterpriseName == null){
    JOptionPane.showMessageDialog(null, "Invalid Name", "Warning",JOptionPane.WARNING_MESSAGE);
    }else{
    EnterpriseDTO enterprise = new EnterpriseDTO(txtEnterpriseName.getText(),cmbEntType.getSelectedItem().toString(), cmbNetType.getSelectedItem().toString());
    ManageAdminDAO.insertEnterprise(enterprise);
    populateEnterpriseTable();
    }   
    }//GEN-LAST:event_submitJButtonActionPerformed

