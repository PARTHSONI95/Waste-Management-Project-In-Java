    private void populateEnterpriseTable(){
    
          ArrayList<EnterpriseDTO> arr = ManageAdminDAO.selectEnterprise();

        for (EnterpriseDTO temp : arr) {
            System.out.println("In Enterprise Admin---");
            System.out.println(temp.getEnterpriseName());
        }

        DefaultTableModel dtm = (DefaultTableModel) tblenterprise.getModel();
        dtm.setRowCount(0);

        for (EnterpriseDTO a : arr) {

            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = a.getEnterpriseName();
            row[1] = a.getNetworkName();
            row[2] = a.getEnterpriseType();
            dtm.addRow(row);
        }
 
    
    
    }
